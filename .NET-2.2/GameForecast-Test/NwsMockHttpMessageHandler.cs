                responseMessage.StatusCode = HttpStatusCode.OK;
                responsePayload = hourlyResponse;
                
                // A zero character immediately after the /BOU/ portion of the path, it indicates we should replicate a negative test scenario.
                if( request.RequestUri.AbsolutePath["/gridpoints/BOU/".Length] == '0' ){
                    // Based on the second character after the /BOU/ replicate a specific negative scenario.
                    switch( request.RequestUri.AbsolutePath["/gridpoints/BOU/".Length+1] ){
                        case '0':
                            responseMessage.StatusCode = HttpStatusCode.NotFound;
                            break;
                        case '1':
                            responseMessage.StatusCode = HttpStatusCode.InternalServerError;
                            break;
                        case '2':
                            // Move the forecast periods to a different key in the JSON. 
                            responsePayload = responsePayload.Replace(
                                        "periods",
